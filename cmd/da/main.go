package main

import (
	"fmt"

	"github.com/deadsy/bender/cpu"
)

//-----------------------------------------------------------------------------

func main() {

	code := []byte{
		0xa9, 0x00, 0xa8, 0x91, 0x02, 0xc8, 0xca, 0xd0,
		0xfa, 0x60, 0x18, 0x36, 0x00, 0x88, 0xd0, 0x01,
	}

	var ofs int
	for ofs < len(code) {
		s, n := cpu.Disassemble(code[ofs:])
		fmt.Printf("%s %d\n", s, n)
		ofs += int(n)
	}

}

